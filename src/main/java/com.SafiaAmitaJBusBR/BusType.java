/**
 * The `BusType` enumeration represents different types of buses that can be used in the system.
 * It includes regular, high-decker, minibus, and double-decker types.
 *
 * @author Safia Amita
 * @version 1.0
 * @since 2023-09-07
 */
package com.SafiaAmitaJBusBR;
public enum BusType {
    /**
     * Represents a regular bus type.
     */
    REGULAR,

    /**
     * Represents a high-decker bus type.
     */
    HIGH_DECKER,

    /**
     * Represents a minibus type.
     */
    MINIBUS,

    /**
     * Represents a double-decker bus type.
     */
    DOUBLE_DECKER;
}
