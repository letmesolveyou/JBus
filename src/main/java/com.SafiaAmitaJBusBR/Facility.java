/**
 * The `Facility` enumeration represents different facilities that can be associated with a bus.
 * It includes AC, lunch, toilet, cool box, WiFi, LCD TV, electric socket, and large baggage.
 *
 * @author Safia Amita
 * @version 1.0
 * @since 2023-09-07
 */
package com.SafiaAmitaJBusBR;

public enum Facility {
    /**
     * Represents the air conditioning facility.
     */
    AC,

    /**
     * Represents the lunch facility.
     */
    LUNCH,

    /**
     * Represents the toilet facility.
     */
    TOILET,

    /**
     * Represents the cool box facility.
     */
    COOL_BOX,

    /**
     * Represents the WiFi facility.
     */
    WIFI,

    /**
     * Represents the LCD TV facility.
     */
    LCD_TV,

    /**
     * Represents the electric socket facility.
     */
    ELECTRIC_SOCKET,

    /**
     * Represents the large baggage facility.
     */
    LARGE_BAGGAGE;
}
